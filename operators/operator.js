// Write a function addSubtract(a, b) that takes two numbers as arguments. Calculate their sum and their difference. Print both results.

function addSubtract(a, b) {
    let sum = a + b;
    let difference = a - b;
    console.log("sum:", sum);
    console.log("difference:", difference);
}
addSubtract(10, 2);

