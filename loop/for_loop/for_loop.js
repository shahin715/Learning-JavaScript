//Print Every 3rd Number from 0 to 30

for (let currentValue = 0; currentValue <= 30; currentValue += 3) {
  console.log(currentValue);
}


//Sum of Numbers from 1 to 5

let sum = 0;
for (let number = 1; number <= 5; number++) {
  sum += number;
}
console.log(sum); // Output: 15

//Print Squares of Numbers from 1 to 4

for (let currentNumber = 1; currentNumber <= 4; currentNumber++) {
  let squaredValue = currentNumber * currentNumber;
  console.log(squaredValue);
}

