// Create an array called fruits with the elements 'banana', 'apple', and 'orange'. Use the unshift method to add 'strawberry' and 'blueberry' to the beginning of this array.

let fruits = ["banana", "apple", "orange"];
fruits.unshift("strawberry", "blueberry");
console.log(fruits); // Output: ['strawberry', 'blueberry', 'banana', 'apple', 'orange']

// You have an empty array called numbers. Use unshift to add the numbers 1, 2, 3, 4, and 5 to the beginning of this array in one go.

let numbers= [];
numbers.unshift(1, 2, 3, 4, 5);
console.log(numbers);// Output: [1, 2, 3, 4, 5]

