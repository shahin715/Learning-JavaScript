//Given an array numbers with the elements [1, 2, 3, 4, 5], use the reverse method to reverse the order of the elements in the array. Print the reversed array.

let numbers= [1, 2, 3, 4, 5];
numbers.reverse();
console.log(numbers); //output: [ 5, 4, 3, 2, 1 ]


// Create an array numbers with the elements [6, 7, 8]. Use the push method to add the elements 9 and 10 to the end of the array. Then, use the reverse method to reverse the array and print the result.

let nums = [6, 7, 8];
nums.push(9, 10);
nums.reverse();
console.log(nums); // Output: [10, 9, 8, 7, 6]
